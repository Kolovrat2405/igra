class TileSet {
public:
	int tileWidth;
	int	tileHeight;
	sf::Texture texImage;
	std::vector<sf::Rect<int>> subRects;
	int firstTileID;
	bool loadFromFile(int firstTileID, std::string pathToTileset)
	{
		this->firstTileID = firstTileID;
		// загружаем тайлсет
		TiXmlDocument tilesetFile(pathToTileset.c_str());
		if (!tilesetFile.LoadFile())//если не удалось загрузить карту
		{
			std::cout << "Loading tileset \"" << pathToTileset << "\" failed." << std::endl; //выдаем ошибку
			return false;
		}

		// source - путь до картинки в контейнере image
		TiXmlElement* image;
		image = tilesetFile.FirstChildElement("tileset")->FirstChildElement("image");
		std::string imagepath = image->Attribute("source");

		// пытаемся загрузить картинку
		sf::Image img;

		if (!img.loadFromFile(imagepath))
		{
			std::cout << "Failed to load tile sheet. Path: " << imagepath << std::endl; //если не удалось загрузить тайлсет-выводим ошибку в консоль
			return false;
		}

		const char* trans = image->Attribute("trans");
		if (trans != nullptr) {
			img.createMaskFromColor(hexToColor(strtol(trans, NULL, 16))); //для маски цвета.сейчас нет маски
		}
		texImage.loadFromImage(img);
		texImage.setSmooth(false);//сглаживание

		// получаем количество столбцов и строк тайлсета
		tileWidth = atoi(tilesetFile.FirstChildElement("tileset")->Attribute("tilewidth"));
		tileHeight = atoi(tilesetFile.FirstChildElement("tileset")->Attribute("tileheight"));
		int columns = texImage.getSize().x / tileWidth;
		int rows = texImage.getSize().y / tileHeight;

		// вектор из прямоугольников изображений (TextureRect)
		subRects.clear();
		for (int y = 0; y < rows; y++)
			for (int x = 0; x < columns; x++)
			{
				sf::Rect<int> rect;

				rect.top = y * tileHeight;
				rect.height = tileHeight;
				rect.left = x * tileWidth;
				rect.width = tileWidth;

				subRects.push_back(rect);
			}
		return true;
	}
	sf::Rect<int>& getRect(int ID) {
		return subRects[ID - firstTileID];
	}
};

